public class RRCdato {

    public int rango;
    public double radio;
    public String scolor;

    /**
     *
     * @param rango
     * @param radio
     * @param scolor
     */
    public RRCdato(int rango, double radio, String scolor) {
        this.radio = radio;
        this.rango = rango;
        this.scolor = scolor;
    }
}
